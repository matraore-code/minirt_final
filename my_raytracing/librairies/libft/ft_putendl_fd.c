#include "libft.h"

void	ft_putendl_fd(char *s)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			ft_putchar_fd(s[i]);
			i++;
		}
		write(1, "\n", 1);
	}
}
